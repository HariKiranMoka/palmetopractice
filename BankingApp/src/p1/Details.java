package p1;

public class Details {
	private String name;
	private String pwd;
	public Details()
	{
		
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	public void setPwd(String pwd)
	{
		this.pwd=pwd;
	}
	public String getName()
	{
		return name;
	}
	public String getPwd()
	{
		return pwd;
	}


}
